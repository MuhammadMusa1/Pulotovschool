# f-строки
name = "Alice"
age = 25
print(f"Hello, my name is {name} and I am {age} years old.")

# Метод format
name = "Bob"
age = 30
print("Hello, my name is {} and I am {} years old.".format(name, age))

# Оператор %
name = "Charlie"
age = 35
print("Hello, my name is %s and I am %d years old." % (name, age))